// Info categories for protocol responses
export type InfoCategory = "pool" | "breakfast" | "phone" | "restaurant" | "coffeeShop" | "balcony" | "spa" | "jacuzzi" | "laundry" | "kidsSection";

export const infoCategories: { value: InfoCategory; label: string }[] = [
  { value: "pool", label: "المسبح" },
  { value: "breakfast", label: "الإفطار" },
  { value: "phone", label: "رقم الهاتف" },
  { value: "restaurant", label: "المطعم" },
  { value: "coffeeShop", label: "كوفي شوب" },
  { value: "balcony", label: "الاطلالة / البلكونة" },
  { value: "spa", label: "السبا" },
  { value: "jacuzzi", label: "الجاكوزي / البانيو" },
  { value: "laundry", label: "خدمة الغسيل" },
  { value: "kidsSection", label: "قسم الأطفال" },
];

export function getProtocolResponse(hotel: HotelBranch, category: InfoCategory): string {
  const categoryLabels: Record<InfoCategory, string> = {
    pool: "المسبح",
    breakfast: "الإفطار",
    phone: "رقم التواصل",
    restaurant: "المطعم",
    coffeeShop: "الكوفي شوب",
    balcony: "الاطلالة والبلكونة",
    spa: "السبا",
    jacuzzi: "الجاكوزي والبانيو",
    laundry: "خدمة غسيل الملابس",
    kidsSection: "قسم الأطفال",
  };

  const value = hotel[category];
  const label = categoryLabels[category];

  if (category === "phone") {
    return `عزيزي الضيف، رقم التواصل مع ${hotel.name} هو: ${hotel.phone}. يسعدنا خدمتكم.`;
  }

  const isUnavailable = value.includes("لا يوجد") || value === "-" || value === "*";
  const isUnderConstruction = value.includes("تحت الإنشاء");
  const isCurrentlyUnavailable = value.includes("غير متاح");

  if (isUnavailable) {
    return `عزيزي الضيف، بخصوص استفساركم عن ${label} في ${hotel.name}، للأسف ${label} غير متوفر حالياً في الفندق. هل بإمكاني مساعدتكم بشيء آخر؟`;
  }

  if (isUnderConstruction) {
    return `عزيزي الضيف، بخصوص ${label} في ${hotel.name}، ${label} حالياً تحت الإنشاء وسيتم الإعلان عن افتتاحه قريباً إن شاء الله. هل بإمكاني مساعدتكم بشيء آخر؟`;
  }

  if (isCurrentlyUnavailable) {
    return `عزيزي الضيف، بخصوص ${label} في ${hotel.name}، الخدمة غير متاحة حالياً. نعتذر عن الإزعاج. هل بإمكاني مساعدتكم بشيء آخر؟`;
  }

  return `عزيزي الضيف، يسعدني إفادتكم بخصوص ${label} في ${hotel.name}:\n${value}\n\nشكراً لتواصلكم مع إدارة الحجز المركزي. هل يوجد استفسار آخر؟`;
}
